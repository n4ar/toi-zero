text = input()  # รับข้อความจากผู้ใช้

result = ""      # สร้างตัวแปรไว้เก็บผลลัพธ์หลังเข้ารหัส
count = 1        # ตัวแปรนับจำนวนตัวอักษรที่เหมือนกันติดกัน

for i in range(1, len(text)):  # วนตั้งแต่ตัวที่ 1 ถึงตัวสุดท้าย (ไม่รวมตัวแรก)
    if text[i] == text[i - 1]:  # ถ้าตัวนี้เหมือนกับตัวก่อนหน้า
        count += 1              # เพิ่มจำนวนตัวอักษรที่ซ้ำ
    else:
        result += str(count) + text[i - 1]  # ต่อผลลัพธ์เป็น "จำนวน + ตัวอักษรก่อนหน้า"
        count = 1  # รีเซ็ตตัวนับ เพราะเจอตัวใหม่แล้ว

# เพิ่มตัวสุดท้ายที่ยังไม่ได้ต่อเข้า result
result += str(count) + text[-1]

print(result)
